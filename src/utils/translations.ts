export const t = (key: string, language: Language, params?: Record<string, string | number>) => {
  let text = translations[language][key] || key;
  
  if (params) {
    Object.entries(params).forEach(([param, value]) => {
      text = text.replace(`{${param}}`, String(value));
    });
  }
  
  return text;
};